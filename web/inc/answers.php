            <p class="answers__box__item__question question">O que é uma Telha Termoacústica?</p>
            <p class="answers__box__item__response response">A Telha Termoacústica, também conhecida como Telha Isotérmica, é composta por duas chapas metálicas com um núcleo isolante (geralmente de EPS, PU ou PIR) entre elas. Esse núcleo proporciona isolamento térmico, ajudando a manter a temperatura interna do ambiente, e isolamento acústico, reduzindo ruídos externos, como o barulho da chuva. É uma opção ideal para quem busca conforto e economia de energia.</p>
        </article>

        <article class="answers__box__item">
